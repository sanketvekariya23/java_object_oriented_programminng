 
public class even_odd_loop {
    public static void main(String[] args) {
        System.out.println("even number");

        for(int i=0; i<50; i++){
            System.out.println(2*i);
        }

        System.out.println("odd  number");
        for(int j=0; j<50; j++)
        {
            System.out.println(2*j+1);
        }
    }
}
